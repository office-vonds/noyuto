#!/bin/bash
# 毎朝cronで実行: 分析→生成→投稿→通知 の循環ループ
# 投稿→分析→対応→投稿 が止まらずに回る設計
cd ~/projects/vonds

LOGFILE=~/claude_cron_post.log
OUTFILE=~/projects/vonds/daily_report.txt
DATE=$(date +%Y/%m/%d)

echo "=== $DATE 実行開始 ===" >> "$LOGFILE"

# ============================================================
# ステップ0: 前日投稿のエンゲージメント分析（改善ループの起点）
# ============================================================
echo "[分析] エンゲージメント取得開始..." >> "$LOGFILE"
ENGAGEMENT_REPORT=$(python3 track_engagement.py --report 2>>"$LOGFILE")
python3 track_engagement.py --update-weights >> "$LOGFILE" 2>&1
echo "[分析] テーマ重み更新完了" >> "$LOGFILE"

# ステップ0.5: インフルエンサー構造分析（週1回: 月曜のみ）
if [ "$(date +%u)" = "1" ]; then
    if ! python3 analyze_influencers.py --update-prompt >> "$LOGFILE" 2>&1; then
        echo "[警告] インフルエンサー分析失敗（X API認証エラーの可能性）。後続ステップは継続" >> "$LOGFILE"
    fi
fi

# ============================================================
# ステップ1: 引用リポスト候補検索
# ============================================================
QUOTES=$(python3 search_quotes.py --top 5 2>/dev/null)

# ============================================================
# ステップ1.5: リプライ候補検索（merged from add-reply-candidates）
# ============================================================
REPLIES=$(python3 search_reply_targets.py --section --no-email 2>/dev/null)

# ============================================================
# ステップ2: 投稿文生成（テーマは重み付き選択=分析結果が反映済み）
# ============================================================
POST=$(/home/noyuto/.local/bin/claude --print "
以下の手順を実行し、結果をテキストで出力してください。Gmailは送信しないでください。

【ステップ1: オリジナル投稿生成（スレッド4本 + note記事）】
1. auto_generate.py を引数なしで実行してランダムなテーマを取得
   ※ テーマはエンゲージメントスコアに基づく重み付き選択
2. prompts/noyuto_persona.txt を読み、そのテーマでNOYUTOとしてXスレッド4本を生成
   - 1本目: フック（断定・インパクトでスクロールを止める）
   - 2本目: 煽り（反論を先回りして挑発する）
   - 3本目: 根拠（体験・事実で証明する）
   - 4本目: 着地（答えは出さず問いで終わる）
3. prompts/learned_patterns.txt を読み、構造パターンを参考にする
4. 同じテーマでnote記事も生成する（Xで書ききれなかった核心を深掘りする長文）

【ステップ2: 保存】
generate_post.py --full --topic テーマ --json で保存してください。
JSONフォーマット: {\"thread\": [\"フック本文\", \"煽り本文\", \"根拠本文\", \"着地本文\"], \"note\": \"note記事本文\"}

重要ルール:
- threadの各要素は投稿文のみ。【1/4 フック】等の構造ラベルは絶対に含めない
- 各ツイートは140文字以上を目指す（Xは全角280文字制限）
- 各ツイートの文中に必ず改行（\\n）を入れること。2〜3文ごとに空行（\\n\\n）を入れる。ベタ書きの壁テキスト禁止。Xで読みやすい改行を意識する
- note記事はmarkdown形式。## 見出しを使う
- #信頼経済 等のハッシュタグはthread最後の着地にのみ付ける

以下のフォーマットで出力してください:

テーマ: （テーマ名）
構造パターン: （使用した構造）

【1/4 フック】
（投稿文）

【2/4 煽り】
（投稿文）

【3/4 根拠】
（投稿文）

【4/4 着地】
（投稿文）

【note記事】
（記事本文の冒頭100文字）...
" 2>>"$LOGFILE")

# ============================================================
# ステップ3: X自動投稿（スレッド）
# ============================================================
echo "[X投稿] スレッド自動投稿開始..." >> "$LOGFILE"
if python3 approve_post.py --auto-thread >> "$LOGFILE" 2>&1; then
    echo "[X投稿] 自動投稿完了" >> "$LOGFILE"
else
    echo "[X投稿] 自動投稿失敗（APIキー要確認）" >> "$LOGFILE"
fi

# ============================================================
# ステップ4: note自動投稿
# ============================================================
echo "[note] 自動投稿開始..." >> "$LOGFILE"
if python3 post_note.py >> "$LOGFILE" 2>&1; then
    echo "[note] 投稿完了" >> "$LOGFILE"
else
    echo "[note] 投稿失敗" >> "$LOGFILE"
fi

# ============================================================
# ステップ5: 日報生成＋Gmail送信
# ============================================================
cat > "$OUTFILE" << REPORT_EOF
━━━━━━━━━━━━━━━━━━
【NOYUTO日報】$DATE
━━━━━━━━━━━━━━━━━━

■ エンゲージメント分析（前日までの実績）
━━━━━━━━━━━━━━━━━━
$ENGAGEMENT_REPORT

━━━━━━━━━━━━━━━━━━
■ 本日のオリジナル投稿（スレッド4本 → X自動投稿済み）
━━━━━━━━━━━━━━━━━━
$POST

━━━━━━━━━━━━━━━━━━
■ 引用リポスト候補 TOP5
━━━━━━━━━━━━━━━━━━
$QUOTES

$REPLIES

━━━━━━━━━━━━━━━━━━
■ 本日のアクション
- X投稿: 自動投稿済み（確認のみ）
- 引用RT: 上記候補から1-2件を手動で選択→引用RT
- リプライ: 上記候補からURLを開いてパターンA or Bをコピペ→送信
- note: 自動投稿済み（確認のみ）

所要時間目安: 5-10分（引用RT・リプライ選択のみ）
REPORT_EOF

python3 send_gmail.py \
    --to office.vonds@gmail.com \
    --subject "【NOYUTO日報】$DATE 投稿完了＋分析レポート" \
    --body-file "$OUTFILE" >> "$LOGFILE" 2>&1

if [ $? -ne 0 ]; then
    echo "Gmail送信失敗。レポートは $OUTFILE に保存済み" >> "$LOGFILE"
fi

echo "=== $DATE 実行完了 ===" >> "$LOGFILE"
